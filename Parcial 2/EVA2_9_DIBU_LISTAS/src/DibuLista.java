    private void cnvDibuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cnvDibuMouseClicked
        // TODO add your handling code here:
        Point pCoor = evt.getPoint();
        lstDoble.agregarNodo(new Nodo(pCoor.x, pCoor.y));
        Graphics gpDibu = cnvDibu.getGraphics();
        gpDibu.drawRect(pCoor.x - 5, pCoor.y - 5, 10, 10);
        if (pPrev != null) {
            gpDibu.drawLine(pPrev.x, pPrev.y, pCoor.x, pCoor.y);
        }
        pPrev = pCoor;

    }//GEN-LAST:event_cnvDibuMouseClicked

