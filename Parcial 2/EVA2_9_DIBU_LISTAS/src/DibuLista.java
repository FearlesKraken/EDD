    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed
        // TODO add your handling code here:
        Graphics gpDibu = cnvDibu.getGraphics();

        int returnVal = seleccionar.showOpenDialog(this);
        String texto = "";

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            
            File archivo = seleccionar.getSelectedFile();
            gpDibu.clearRect(0, 0, 800, 600);
            lstDoble.vaciar();
            try {
                FileReader lector = new FileReader(archivo.getName());
                BufferedReader contenido = new BufferedReader(lector);
                texto = contenido.readLine();
                String[] partes = texto.split("-");
                for (int i = 0; i < partes.length-1; i+=2) {
                    x = Integer.parseInt(partes[i]);
                    y = Integer.parseInt(partes[i+1]);
                    lstDoble.agregarNodo(new Nodo(x, y));
                    
                }
                
                gpDibu.drawRect(x, y, 10, 10);
                if (pPrev != null) {
                    gpDibu.drawLine(pPrev.x, pPrev.y, x, y);
                }
                pPrev.x = x;
                pPrev.y = y;

            } catch (Exception e) {
                System.out.println(e);
            }
            System.out.println("Abriendo: " + archivo.getName());
            System.out.println(texto);
        }
    }//GEN-LAST:event_btnAbrirActionPerformed

